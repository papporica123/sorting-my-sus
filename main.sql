SELECT department_id AS "DEPARTMENT CODE",
COUNT(*) AS "NUMBER OF EMPLOYEES"
FROM DEPARTMENT
GROUP BY department_id;

SELECT department_id, SUM(SALARY)
FROM DEPARTMENT GROUP BY department_id;

SELECT department_id AS "DEPARTMENT CODE",
SUM(SALARY) AS "TOTAL SALARY"
FROM DEPARTMENT
WHERE MANAGER_ID="27"
GROUP BY department_id;

